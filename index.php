<br>WHY PARTICIPATE IN BITSMUN?<br><br>
BITSMUN has always stood out from the rest of the MUN crowd for a great deal of reasons. We constantly strive to bring the spirit of innovation and intensive research via a realistic, academic simulation of the thriving international diplomatic community of the United Nations, and provide our delegates with a deep and lasting insight into the functioning of those distinguished international bodies that play such an important role in running the modern world. In addition, BITSMUN also offers its participants excellent social events and the Goan environment will surely leave a memorable experience.<br><br>
WHO CAN TAKE PART IN BITSMUN?<br>
All students in undergraduate or graduate programmes and high school students are eligible to participate in BITSMUN.<br><br>
DO I NEED MUN EXPERIENCE?<br>
Absolutely not - a considerable number of participants at BITSMUN every year have had no experience with MUNs before. Enthusiasm is the characteristic we look for the most.<br><br>
IS BITSMUN OPEN FOR DELEGATIONS?<br>
Participation in BITSMUN depends on an individual application. If a group of students are willing to participate, then they may file their individual applications in a bundle and pay the application fee in one sum. In that case, we would like to ask you to pass this information on to us as soon as possible, so that we can avoid confusion.<br><br>
WILL THERE BE A DRESS CODE DURING THE CONFERENCE?<br>
A formal dress code will be strictly enforced during the conference. Business apparel is required during all sessions, meaning suits for men and business dress for women. Casual wear is suitable for all other events.<br><br>
IS ACCOMMODATION PROVIDED?<br>
BITSMUN offers accommodation on a first come first serve basis to its participants in Goa. There will be a separate registration form for accommodation which will be put up shortly.<br><br>
DOES BITSMUN PROVIDE MEALS?<br>
BITSMUN provides breakfast and lunch every day. The social events taking place would also have food provided.<br><br>
