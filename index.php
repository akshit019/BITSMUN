A Model United Nations conference is a simulation of the United Nations, in which students learn about diplomacy, international relations and the United Nations. MUNs, as they are referred to, involve critical thinking, teamwork and leadership abilities. A participant in a MUN conference, referred to as a delegate, is taught several soft skills such as research, debating and writing skills. MUNs also test how delegates can ‘think on their feet’, in lay-man terms.<br><br>
The delegates are assigned countries or occasionally other organisations or important political figures. They are presented with the agenda/topic(s) that their committee shall discuss, in advance. Delegates then conduct research on the same before the conference, from their position’s perspective and then formulate their stance on the topic at hand, during the conference. They shall then indulge in formal debate in committee, while staying true to their initial stance and position that they represent. After the conference is over, the best performing delegates are recognised with awards.<br><br>
